void solve() {
    ll n;
    cin >> n;

    ll l = 1, h = n, mid = 1;

    while(l <= h){
        mid = (l+h)/2;

        ll a = ((mid == 1)?1e9 : query(mid-1));
        ll b = query(mid);
        ll c = ((mid == n)?1e9 : query(mid+1));
        //cerr << a << " "<< b << " "<< c<< ln;
        if(a > b && c > b)break;
        if(a > b)l = mid+1;
        if(a < b)h = mid-1;
    }
    cout << "! "<< mid << endl;
}

